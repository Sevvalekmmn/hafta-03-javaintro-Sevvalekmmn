public class Faktoriyel {
    public static void main(String[] args) {
        // Faktöriyel hesaplama (5! = 5 × 4 × 3 × 2 × 1)
        int sonuc = 5 * 4 * 3 * 2 * 1;

        // Ara adımları göster
        System.out.println("5! = 5 × 4 × 3 × 2 × 1");
        System.out.println("Sonuç = " + sonuc);
    }
}
